    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        try {
            int currentQuantity, quantity;
            currentQuantity = Integer.valueOf(txt_qtd_actually.getText());
            quantity = Integer.valueOf(txt_quantity.getText());
            // Global variable
            qtd_atualizada = currentQuantity + quantity;
            ProductDAO pdao = new ProductDAO();
            // idProtudo Global
            pdao.addStock(idProduto, qtd_atualizada);
            new CleanFields().cleanForm(panelPersonalData);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao adicionar ao estoque do produto!" +e);
        }
    }//GEN-LAST:event_btn_addActionPerformed

