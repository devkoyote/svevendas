    public void listTable() {
        ProductDAO pdao = new ProductDAO();
        List<Product> list = pdao.listProduct();
        // Call DefaultTableModel
        DefaultTableModel dtb = (DefaultTableModel) table1.getModel();
        // Set Colums rows
        dtb.setNumRows(0);
        // for 
        for (Product prd : list) {
            // put dtb -> list
            dtb.addRow(new Object[]{
                prd.getId(),
                prd.getDescribe(),
                prd.getPrice(),
                prd.getQtd_Stock(),
                prd.getSuppliers().getName()

            });
        }
    }

